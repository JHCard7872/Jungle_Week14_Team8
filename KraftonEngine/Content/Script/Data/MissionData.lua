-- ============================================================================
-- MissionData — 미션 발급 상수 (require 모듈, 읽기 전용)
-- [역할] 임계치 방식 미션의 기준값. 고정 미션 풀은 없다 — 발급 시점에 살아있는
--        래그돌을 집계해서 동적으로 만든다 (MissionManager.IssueNext 참고).
-- [사용법] local Mission = require("Data/MissionData")
-- [특이사항] 발급 규칙: 보유 수 >= minCount인 타입 중 랜덤(목표 = minCount),
--            충족 타입이 없으면 최다 보유 타입(목표 = 그 보유 수) — 달성 불가능한
--            미션이 나오지 않게 하는 장치다.
--            미션별 제한시간은 안 둔다 (기획의 열린 질문을 설계에서 종결한 결정).
-- ============================================================================

return {
    minCount        = 3,    -- 미션 후보가 되는 최소 보유 수 = 기본 목표 수      -- 임시 밸런스
    rewardPerBody   = 100,  -- 미션 달성 보너스 = 목표 수 × 이 값 (need × 100)  -- 임시 밸런스
    reissueInterval = 1.0,  -- 미션이 비었을 때 재발급을 시도하는 주기(초). 활성 중엔 시도 안 함
}
